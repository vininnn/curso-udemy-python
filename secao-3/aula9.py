adicao = 10 + 10
print('Adição:', adicao)

subtracao = 10 - 10
print('Subtração:', subtracao)

multiplicacao = 10 * 10
print('Multiplicação:', multiplicacao)

divisao = 10 / 3
print('Divisão:', divisao)

divisao_inteira = 10 // 3
print('Divisão Interia:', divisao_inteira)

resto_da_divisao = 10 % 3
print('Resto da Divisão/Módulo:', resto_da_divisao)

potenciacao = 10 ** 3 # 10/3 -> 10-9 = 1
print('Potenciação:', potenciacao)

