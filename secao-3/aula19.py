# Operadores de comparação (relacionais)
# OP    Significado
# >     maior que  
# <     menor que
# >=    maior ou igual que
# <=    menor ou igual que
# ==    igual a
# !=    diferente de

maior = 2 < 1
menor = 1 < 2
maior_ou_igual = 2 >= 1 
maior_ou_igual = 2 >= 2 
menor_ou_igual = 1 <= 2
menor_ou_igual = 1 <= 1
igual = 'a' == 'a'
diferente = 'a' != 'b'
