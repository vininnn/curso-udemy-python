# Python = Linguagem de programação
# Tipagem = Dinâmica e forte

# Strings é um tipo de texto entre aspas
# string -> str -> texto

# Aspas simples
print('Hello, World!')
print('Hello, "World!"')

# Aspas duplas
print("Hello, World!")
print("Hello, 'World!'")

# Escape
print('Hello, \"World!\"')

# r
print(r'Hello, \"World!\"')
