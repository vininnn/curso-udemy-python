# Tipo de dados bool (boolean)
# Ao questionar coisas no programa é dado a possibilidade de duas respostas
# True (sim/verdadeiro) False(não/falso)

# Existem vários operadores para questionar
# '==' é um operador lógico que questiona a igualdade entre valores

print(2 == 2) #True
print(2 == 3 - 1) #True
print(2 == 3) #False
print(type(2 == 2))
